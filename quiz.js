var globalCnt = 0;//this is a global variable (ie. its accessible throughout the whole document) that counts the number of questions.
var quiz ={//this is a class because it contains functions.
  'initialize' : function(){//this is the function (initialize) we are calling when he HTML page loads
    $.each(myQuestions, function(i,v){
      globalCnt++;//increments by 1 the global variable count.
      var myQuestion = myQuestions[i]['question'];
      var _formElements = '';
      $.each(myQuestions[i]['answers'], function(si,sv){
        _formElements += '<div class="form-check">';//this is all outputs.
          _formElements += si + '&nbsp;<input class="radioButtons " data-correctAnswer="' + myQuestions[i]['correctAnswer'] + '" type="radio" name="' + i + '" value="' + si + '"/>';
          _formElements += '<label>';
            _formElements += myQuestions[i]['answers'][si];
          _formElements += '</label>';
        _formElements += '</div>';
      });

      $('#quiz_form').append('<p>' + myQuestion + '</p><p>' + _formElements + '</p>');
    });
    var _formButton = '<a class="btn btn-primary" id="submitButton" href="javascript:quiz.submit();">Submit</a>';
    $('#quiz_form').append(_formButton);
  },//end of initialize function
  'submit' : function(){
    $('#submitButton').remove();//this where you remove the submitButton once its clicked. The parentheses is used as a get element on submitButton.
    var correctCnt = 0; //declare correct count variable and set it to zero
    $('.radioButtons').each(function(i,v){ //loop through each radio button with a class name of radioButtons
      var theirAnswer = '';
      var correctAnswer = '';
      if ($(this).is(':checked')){ //check if the radio button is checked/clicked
        theirAnswer = ($(this).val());//if the radio button is checked then assign that radio button's value to variable
        correctAnswer = $(this).attr('data-correctAnswer');//check what the correct value is for the radio button
        if (correctAnswer == theirAnswer){//compare the radio button value checked to the correct answer. if it equals each other then increment the correctCnt by 1.
          correctCnt++;
        }
      }
    });

    $('#quiz_form').append('<p>Congratulations, you got ' + correctCnt + '/' + globalCnt + '</p>s');
  }
};
